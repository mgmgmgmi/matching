    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Like  $like
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $authUser = Auth::user();
        Like::where('from_user_id', $authUser->id)->where('to_user_id', $id)->delete();

        return redirect('users/' . $id);
